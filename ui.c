// Plays through the animations frame by frame
void init_splash() 
{
    int DELAY = 200;
    int frame_num, i, cyc = 0;
    for(frame_num = 0; frame_num < 12; frame_num++) {
        while(cyc++ < DELAY) {
            for(i = 0; i < OLED_BUF_SIZE; i++) {
                display_buffer[i] = frames[i + frame_num*OLED_BUF_SIZE];
            }
            oled_put_buffer();
        }
        cyc = 0;
    }
}

